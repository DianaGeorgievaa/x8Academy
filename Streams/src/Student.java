package students;

public class Student {

	int grade;
	Gender gender;
	int age;
	String email;

	public Student() {
	}

	public int getGrade() {
		return grade;
	}

	public Gender getGender() {
		return gender;
	}

	public int getAge() {
		return age;
	}

	public String getEmail() {
		return email;
	}
}
